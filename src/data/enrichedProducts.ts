const productKey = (product: Pick<Product, 'brand' | 'name'>): string =>
  `${product.brand} ${product.name}`.toLowerCase().replace(/[^a-z0-9]+/g, ' ').trim();

export const enrichProducts = (products: Product[]): Product[] => {
  const existingKeys = new Set(products.map(productKey));
  const mergedProducts = products.map((product) => {
    const enrichment = enrichedProducts.find((candidate) => productKey(candidate) === productKey(product));
    return enrichment ? { ...enrichment, ...product, ...(!product.features && { features: enrichment.features }) } : product;
  });

  const missingFallbackProducts = enrichedProducts.filter((product) => !existingKeys.has(productKey(product)));
  return [...mergedProducts, ...missingFallbackProducts];
};
